// if else syntax
if (true) {
  console.log("if statement run");
}

if (false) {
  console.log("if statement run");
}

const age = 25;

if (age > 20) {
  console.log("age is greater than 20");
}

if (age >= 25) {
  console.log("age is greater or equal than 25");
}

if (age < 26) {
  console.log("age is less than 26");
}

if (age === 25) {
  console.log("age is equal to 25");
}

if (age != 25) {
  console.log("age is not equal to 25");
}

// if else
if (age >= 27) {
  console.log("age is greater");
} else {
  console.log("age is not greater");
}
